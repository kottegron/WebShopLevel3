package BO;

public class Items {
	
	private int E;
	private int C;
	private int P;

	public Items() {
		E = 0;
		C = 0;
		P = 0;
	}
	
	
	public int getE() {
		return E;
	}

	public void setE(int e) {
		E = E + e;
	}

	public int getC() {
		return C;
	}

	public void setC(int c) {
		C = C + c;
	}

	public int getP() {
		return P;
	}

	public void setP(int p) {
		P = P + p;
	}

	
	
	
	

}
